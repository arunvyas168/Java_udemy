package com.company;

public class Main {

    public static void main(String[] args) {
        int myIntValue = 5/3;
        float myFloatValue = 5f/3;
        double myDoubleValue = 5d/3;
        System.out.println("the value of myIntValue = " + myIntValue);
        System.out.println("the value of myFloatValue = " + myFloatValue);
        System.out.println("the value of myDoubleValue = " + myDoubleValue);

    }
}
